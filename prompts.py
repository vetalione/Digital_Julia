def build_scenario_prompt(user_profile: dict, settings: dict, user_input: str | None = None) -> str:
    """Строит промпт для генерации сценария на основе профиля юзера и настроек."""
    style_key = settings.get("style", "expert")
    style = REELS_STYLES.get(style_key, REELS_STYLES["expert"])
    
    target_key = settings.get("target", "leads")
    target = AUDIENCE_TARGETS.get(target_key, AUDIENCE_TARGETS["leads"])
    
    duration = settings.get("duration", "30")
    duration_desc = DURATIONS.get(duration, DURATIONS["30"])

    prompt = f"""Создай сценарий Reels на основе следующей информации:

👤 ПРОФИЛЬ АВТОРА:
- Продукт/услуга: {user_profile.get('product', 'не указано')}
- Ниша: {user_profile.get('niche', 'не указана')}
- Целевая аудитория: {user_profile.get('audience', 'не указана')}

🎨 ВЫБРАННЫЙ СТИЛЬ:
{style['name']}
{style['prompt_detail']}

🎯 КУДА ВЕДЁМ АУДИТОРИЮ:
{target}

⏱ ДЛИТЕЛЬНОСТЬ:
{duration_desc}
"""
    
    if user_input:
        if style_key == "news":
            prompt += f"""
📰 ВЫБРАННАЯ НОВОСТЬ:
{user_input}

Сценарий ДОЛЖЕН быть построен ВОКРУГ ЭТОЙ КОНКРЕТНОЙ НОВОСТИ.
Новость нужно подать через призму ниши автора, его продукта/услуги и интересов его ЦА.
Автор должен выглядеть как эксперт в своей нише, который комментирует новость со знанием дела.

Структура:
1. ХУКА (0-3 сек) — провокация, цифра или вопрос в лоб по теме новости. Без вступлений.
2. ПОЯСНЕНИЕ (3-10 сек) — снимаем напряжение, объясняем что это реальная история, создаём доверие и интригу.
3. СУТЬ — факты и механика новости, просто и живо. Каждый абзац — новая мысль.
4. ПОЛЬЗА / ГЛАВНАЯ МЫСЛЬ — как эта новость влияет на ЦА автора, что им с этим делать.
5. ВОПРОС АУДИТОРИИ — личный, конкретный, задевающий, хочется ответить.
6. ПОДПИСКА + ПРИЗЫВ — причина подписаться + действие.

Подача: энергичная, как будто автор первым узнал новость и спешит поделиться. НЕ выдумывай факты — используй ту новость, которая выбрана.
"""
        else:
            prompt += f"""
📝 ЗАДУМКА/СЦЕНАРИЙ АВТОРА:
{user_input}

Возьми задумку автора как основу и создай на её базе профессиональный сценарий, усилив хуки, удержание и CTA.
"""
    else:
        prompt += """
Автор не предоставил свою задумку — создай сценарий с нуля, учитывая профиль, нишу и ЦА автора.
"""

    return prompt
